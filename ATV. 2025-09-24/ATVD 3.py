#Solicite ao usuário uma temperatura em graus Celsius.
#Converta para Fahrenheit.

celsius = int(input('Me informe a temperatura atual, em graus Celsius, na cidade de Natal/RN'))

fahrenheit = celsius * 9 /5 + 32

print(f'A temperatura atual em Natal/RN está por volta de {celsius}°C e {fahrenheit}°F.')